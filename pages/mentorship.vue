<script setup lang="ts">
import { reactive, ref } from "vue";

const supabase = useSupabaseClient();

const form = reactive({
  name: "",
  email: "",
  role: "",
  message: "",
});

const loading = ref(false);
const success = ref(false);
const error = ref<string | null>(null);

useHead({
  title: "Mentorship Opportunities — Jennifer Payan",
  meta: [
    {
      name: "description",
      content: "Connect with Jennifer Payan to ask questions about becoming an archivist, navigating library science (MLIS) programs, and finding career opportunities.",
    },
  ],
});

async function onSubmit() {
  error.value = null;
  success.value = false;

  if (!form.name || !form.email || !form.role || !form.message) {
    error.value = "Please fill in name, email, role, and your question.";
    return;
  }

  loading.value = true;
  try {
    const { error: insertError } = await supabase
      .from("contact_messages")
      .insert([
        {
          name: form.name,
          email: form.email,
          organization: `Mentorship Inquiry - ${form.role}`,
          message: form.message,
        },
      ]);

    if (insertError) throw insertError;

    success.value = true;
    form.name = "";
    form.email = "";
    form.role = "";
    form.message = "";
  } catch (e: any) {
    error.value = e?.message || "Something went wrong. Please try again.";
  } finally {
    loading.value = false;
  }
}
</script>

